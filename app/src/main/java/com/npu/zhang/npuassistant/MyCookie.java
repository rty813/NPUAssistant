public class MyCookie {
    private String AXCookie;
    private String JWCookie;

    public void setAXCookie(String AXCookie) {
        this.AXCookie = AXCookie;
    }

    public void setJWCookie(String JWCookie) {
        this.JWCookie = JWCookie;
    }

    public String getAXCookie() {
        return AXCookie;
    }

    public String getJWCookie() {
        return JWCookie;
    }
}
